package BasicPrograms;

public class LocalVariable {
	
	//LocalVariable declare inside class and inside methods

	public static void main(String[] args) {
		byte b = 10;  						//this is give you values in output because it is declare and define
		short s = 20;
		int i = 50;
		long l = 15;
		float f = 25.23f;
		double d = 77.664;
		char c = '#';						//if u can use char, so u will give alphabet or symbols
		boolean b1 = true;

		System.out.println(b);		//if u want to call local Variables so need to create object direct call them in SOP.
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b1);
		
	}

}
